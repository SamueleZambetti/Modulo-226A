//Public class con nome upper e deve iniziare con la maiuscola
public class HelloWorld{
	//Entry point (da dove parte il programma)
	public static void main(String[] args){
		//Istruzione print non va a capo
		System.out.print("HelloWorld!!");
		System.out.print("HelloClass!!");
		
		//Istruzione print va a capo la riga sotto (ln = new line)
		System.out.println("HelloWorld!!");
		System.out.println("HelloClass!!");
		
	}
}